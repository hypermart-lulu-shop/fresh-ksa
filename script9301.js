// ---------- Scroll fade-in ----------
let io;
function observeFades() {
  if (!io) {
    io = new IntersectionObserver(
      (entries) => {
        entries.forEach((e) => {
          if (e.isIntersecting) {
            e.target.classList.add("in");
            io.unobserve(e.target);
          }
        });
      },
      { threshold: 0.1 }
    );
  }
  document.querySelectorAll(".scroll-fade:not(.in)").forEach((el) => io.observe(el));
}

