let flashEndsAtMs = Date.now() + 48 * 3600000;

function startFlashBannerClock() {
  const el = document.getElementById("flash-countdown");
  const colon = `<span class="text-lg font-semibold px-2 text-white/40">∶</span>`;
  function seg(value, lbl) {
    return `
      <div class="flex flex-col items-center min-w-[3.85rem]" dir="ltr">
        <span class="countdown-digit rounded-xl bg-black/55 border border-white/10 px-3 py-2 text-3xl md:text-[2.125rem] font-black leading-none text-white shadow-inner">${String(
          value,
        ).padStart(2, "0")}</span>
        <span class="mt-2 text-[9px] font-bold uppercase tracking-[0.32em] text-white/48">${lbl}</span>
      </div>
    `;
  }
  function tick() {
    if (!el) return;
    const diff = Math.max(0, flashEndsAtMs - Date.now());
    const d = Math.floor(diff / 86400000);
    const h = Math.floor((diff % 86400000) / 3600000);
    const m = Math.floor((diff % 3600000) / 60000);
    const s = Math.floor((diff % 60000) / 1000);
    el.innerHTML =
      seg(d, t("flash.d")) +
      colon +
      seg(h, t("flash.h")) +
      colon +
      seg(m, t("flash.m")) +
      colon +
      seg(s, t("flash.s"));
  }
  tick();
  setInterval(tick, 1000);
}

