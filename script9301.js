function initStickyInstall() {
  const bar = document.getElementById("sticky-install");
  const dismiss = document.getElementById("sticky-dismiss");
  if (!bar) return;
  const dismissedPref = () => localStorage.getItem("lulu-hide-sticky") === "1";

  const applyStickyState = () => {
    const isMobileUp = window.matchMedia?.("(max-width: 1023px)")?.matches;
    const dismissed = dismissedPref();
    bar.hidden = !isMobileUp || dismissed;
    syncStickyPadding();
  };

  if (dismissedPref()) applyStickyState();
  else window.setTimeout(applyStickyState, 1400);

  dismiss?.addEventListener("click", () => {
    localStorage.setItem("lulu-hide-sticky", "1");
    bar.hidden = true;
    syncStickyPadding();
  });

  window.addEventListener("resize", applyStickyState);
}

